function generateBasicYaml(dataset: DatasetDetail): string {
  const { dbName, tblName, platformType, platformId, jdbc, hdfsBase, columns } = getDatasetParts(dataset)
  const dbType = platformType === "mysql" ? "mysql" : platformType === "postgresql" || platformType === "greenplum" ? "postgresql" : platformType

  return `# Kestra Flow: Basic JDBC Extract → HDFS Load
# Source: ${dataset.platform.name} (${platformId})
# Table: ${dbName}.${tblName}
# Generated by Argus Catalog

id: ingest-${platformId}-${tblName}
namespace: argus.catalog

description: |
  Extract data from ${dbName}.${tblName} via JDBC
  and upload to HDFS as a single file.

tasks:
  - id: extract
    type: io.kestra.plugin.jdbc.${dbType}.Query
    url: "${jdbc.url}"
    username: "\${secret('${platformId.toUpperCase().replace(/-/g, '_')}_USERNAME')}"
    password: "\${secret('${platformId.toUpperCase().replace(/-/g, '_')}_PASSWORD')}"
    sql: |
      SELECT ${columns}
      FROM ${dbName}.${tblName}
    store: true

  - id: convert-to-parquet
    type: io.kestra.plugin.serdes.avro.AvroToParquet
    from: "{{ outputs.extract.uri }}"
    compressionCodec: SNAPPY

  - id: upload-to-hdfs
    type: io.kestra.plugin.fs.hdfs.Upload
    from: "{{ outputs['convert-to-parquet'].uri }}"
    to: "${hdfsBase}/data.parquet"

triggers:
  - id: daily-schedule
    type: io.kestra.plugin.core.trigger.Schedule
    cron: "0 2 * * *"  # Daily at 02:00
`
}

function generateSparkYaml(dataset: DatasetDetail): string {
  const { dbName, tblName, platformType, platformId, jdbc, hdfsBase, pkColumn } = getDatasetParts(dataset)

  return `# Kestra Flow: PySpark Partitioned Load
# Source: ${dataset.platform.name} (${platformId})
# Table: ${dbName}.${tblName}
# Generated by Argus Catalog

id: spark-ingest-${platformId}-${tblName}
namespace: argus.catalog

description: |
  Use PySpark to read ${dbName}.${tblName} via JDBC,
  partition by date, and write to HDFS as Parquet.

inputs:
  - id: target_date
    type: STRING
    defaults: "{{ now() | dateAdd(-1, 'DAYS') | date('yyyy-MM-dd') }}"
    description: "Date to process (default: yesterday)"

tasks:
  - id: spark-load
    type: io.kestra.plugin.spark.PythonSubmit
    runner: PROCESS
    mainScript: |
      from pyspark.sql import SparkSession
      from pyspark.sql.functions import lit

      spark = SparkSession.builder \\
          .appName("kestra-${platformId}-${tblName}") \\
          .getOrCreate()

      target_date = "{{ inputs.target_date }}"

      # Read from source with date filter
      df = spark.read \\
          .format("jdbc") \\
          .option("url", "${jdbc.url}") \\
          .option("driver", "${jdbc.driver}") \\
          .option("dbtable", "(SELECT * FROM ${dbName}.${tblName} WHERE DATE(last_update) = '{0}') AS t".format(target_date)) \\
          .option("user", "{{ secret('${platformId.toUpperCase().replace(/-/g, '_')}_USERNAME') }}") \\
          .option("password", "{{ secret('${platformId.toUpperCase().replace(/-/g, '_')}_PASSWORD') }}") \\
          .load()

      print(f"Read {df.count()} rows for {target_date}")

      # Write with date partition
      df.withColumn("dt", lit(target_date)) \\
          .write \\
          .mode("append") \\
          .partitionBy("dt") \\
          .parquet("${hdfsBase}")

      print(f"Written to ${hdfsBase}/dt={target_date}")
      spark.stop()

triggers:
  - id: daily-schedule
    type: io.kestra.plugin.core.trigger.Schedule
    cron: "0 3 * * *"  # Daily at 03:00
`
}

function generateShellYaml(dataset: DatasetDetail): string {
  const { dbName, tblName, platformType, platformId, jdbc, hdfsBase, columns } = getDatasetParts(dataset)
  const dbType = platformType === "mysql" ? "mysql" : platformType === "postgresql" || platformType === "greenplum" ? "postgresql" : platformType

  return `# Kestra Flow: Shell + HDFS Commands
# Source: ${dataset.platform.name} (${platformId})
# Table: ${dbName}.${tblName}
# Generated by Argus Catalog

id: shell-ingest-${platformId}-${tblName}
namespace: argus.catalog

description: |
  Extract ${dbName}.${tblName} via JDBC to local file,
  create HDFS partition directory, and upload.

inputs:
  - id: target_date
    type: STRING
    defaults: "{{ now() | dateAdd(-1, 'DAYS') | date('yyyy-MM-dd') }}"

tasks:
  - id: extract-to-csv
    type: io.kestra.plugin.jdbc.${dbType}.Query
    url: "${jdbc.url}"
    username: "\${secret('${platformId.toUpperCase().replace(/-/g, '_')}_USERNAME')}"
    password: "\${secret('${platformId.toUpperCase().replace(/-/g, '_')}_PASSWORD')}"
    sql: |
      SELECT ${columns}
      FROM ${dbName}.${tblName}
      WHERE DATE(last_update) = '{{ inputs.target_date }}'
    store: true

  - id: create-hdfs-partition
    type: io.kestra.plugin.scripts.shell.Commands
    commands:
      - hdfs dfs -mkdir -p ${hdfsBase}/dt={{ inputs.target_date }}

  - id: upload-to-partition
    type: io.kestra.plugin.fs.hdfs.Upload
    from: "{{ outputs['extract-to-csv'].uri }}"
    to: "${hdfsBase}/dt={{ inputs.target_date }}/${tblName}.csv"

  - id: verify
    type: io.kestra.plugin.scripts.shell.Commands
    commands:
      - hdfs dfs -ls ${hdfsBase}/dt={{ inputs.target_date }}/
      - echo "Upload complete for {{ inputs.target_date }}"

triggers:
  - id: daily-schedule
    type: io.kestra.plugin.core.trigger.Schedule
    cron: "0 2 * * *"
`
}

function generateSqlYaml(dataset: DatasetDetail): string {
  const { dbName, tblName, platformType, platformId, jdbc, hdfsBase } = getDatasetParts(dataset)

  return `# Kestra Flow: Hive/Trino SQL Partitioned Load
# Source: ${dataset.platform.name} (${platformId})
# Table: ${dbName}.${tblName}
# Generated by Argus Catalog

id: sql-ingest-${platformId}-${tblName}
namespace: argus.catalog

description: |
  Use Hive/Trino SQL to create partitions and INSERT data
  from the source ${dbName}.${tblName} table.

inputs:
  - id: target_date
    type: STRING
    defaults: "{{ now() | dateAdd(-1, 'DAYS') | date('yyyy-MM-dd') }}"

tasks:
  - id: create-external-table
    type: io.kestra.plugin.jdbc.trino.Query
    url: "jdbc:trino://<TRINO_HOST>:8080/hive/catalog_${platformId.replace(/-/g, '_')}"
    username: "<TRINO_USER>"
    sql: |
      CREATE TABLE IF NOT EXISTS ${tblName} (
        ${dataset.schema_fields.map(f => `${f.field_path} VARCHAR`).join(",\n        ")}
      )
      WITH (
        format = 'PARQUET',
        partitioned_by = ARRAY['dt'],
        external_location = '${hdfsBase}'
      )
    disabled: true  # Run once to create table, then disable

  - id: add-partition
    type: io.kestra.plugin.jdbc.trino.Query
    url: "jdbc:trino://<TRINO_HOST>:8080/hive/catalog_${platformId.replace(/-/g, '_')}"
    username: "<TRINO_USER>"
    sql: |
      -- Trino automatically discovers partitions for Hive tables
      -- For manual partition management:
      CALL system.sync_partition_metadata(
        schema_name => 'catalog_${platformId.replace(/-/g, '_')}',
        table_name => '${tblName}',
        mode => 'ADD'
      )

  - id: insert-data
    type: io.kestra.plugin.jdbc.trino.Query
    url: "jdbc:trino://<TRINO_HOST>:8080/hive/catalog_${platformId.replace(/-/g, '_')}"
    username: "<TRINO_USER>"
    sql: |
      INSERT INTO ${tblName}
      SELECT
        ${dataset.schema_fields.map(f => `CAST(${f.field_path} AS VARCHAR)`).join(",\n        ")},
        '{{ inputs.target_date }}' AS dt
      FROM ${platformType}.${dbName}.${tblName}
      WHERE DATE(last_update) = DATE '{{ inputs.target_date }}'

  - id: verify-count
    type: io.kestra.plugin.jdbc.trino.Query
    url: "jdbc:trino://<TRINO_HOST>:8080/hive/catalog_${platformId.replace(/-/g, '_')}"
    username: "<TRINO_USER>"
    sql: |
      SELECT count(*) AS row_count
      FROM ${tblName}
      WHERE dt = '{{ inputs.target_date }}'
    store: true

triggers:
  - id: daily-schedule
    type: io.kestra.plugin.core.trigger.Schedule
    cron: "0 4 * * *"  # Daily at 04:00
`
}

